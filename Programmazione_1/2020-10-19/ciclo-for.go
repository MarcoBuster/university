package main

func main() {
	// For 0-ario
	// ciclo infinito
	for {
		// Le istruzioni qui vengono eseguite per sempre
	}

	// For 1-ario
	// equivalente while
	condizione := false
	for condizione {
		// Le istruzioni qui vengono eseguite fino a quando la condizione è vera
	}

	// For 2-ario (3-ario troncato)
	for i1 := 0; i1 != 0; {
		// Dichiara i1 e gli assegna 0, poi controlla la condizione ed esegue fino a quando la condizione è vera
	}

	// For 3-ario
	for i2 := 0; i2 != 0; i2++ {
		// Dichiara i2 e gli assegna 0, poi controlla la condizione, esegue il codice e infine i2++
	}
}
